-- EMPLOYEES01 테이블 생성
CREATE TABLE EMPLOYEES01
( EMPLOYEE_ID    NUMBER(6)     
, FIRST_NAME     VARCHAR2(20)  
, LAST_NAME      VARCHAR2(25) CONSTRAINT EMP01_LAST_NAME_NN NOT NULL    -- NULL 불가능
, EMAIL          VARCHAR2(25) CONSTRAINT EMP01_EMAIL_NN NOT NULL        -- NULL 불가능
, PHONE_NUMBER   VARCHAR2(20)
, HIRE_DATE      DATE         CONSTRAINT EMP01_HIRE_DATE_NN NOT NULL    -- NULL 불가능
, JOB_ID         VARCHAR2(10) CONSTRAINT EMP01_JOB_NN NOT NULL          -- NULL 불가능
, SALARY         NUMBER(8,2)
, COMMISSION_PCT NUMBER(2,2)
, MANAGER_ID     NUMBER(6)
, DEPARTMENT_ID  NUMBER(4)
, CONSTRAINT EMP01_EMP_ID_PK    PRIMARY KEY(EMPLOYEE_ID)                -- EMPLOYEE_ID 를 식별키로 지정
, CONSTRAINT EMP01_EMAIL_UK     UNIQUE(EMAIL)                           -- EMAIL 을 UNIQUE 로 지정
, CONSTRAINT EMP01_SALARY_MIN   CHECK(SALARY > 0)                       -- SALARY > 0 으로 제약조건 설정
, CONSTRAINT EMP01_MANAGER_FK   FOREIGN KEY (MANAGER_ID)    REFERENCES EMPLOYEES01 (EMPLOYEE_ID)     
-- MANAGER_ID 를 FK 로 지정, EMPLOYEES01 테이블에서 EMPLOYEE_ID 컬럼을 참조
, CONSTRAINT EMP01_JOB_FK       FOREIGN KEY (JOB_ID)        REFERENCES JOBS01 (JOB_ID)
-- JOB_ID 를 FK 로 지정, JOBS01 테이블에서 JOB_ID 컬럼을 참조
, CONSTRAINT EMP01_DEPT_FK      FOREIGN KEY (DEPARTMENT_ID) REFERENCES DEPARTMENTS01 (DEPARTMENT_ID)
-- DEPARTMENT_ID 를 FK 로 지정, DEPARTMENTS01 테이블에서 DEPARTMENT_ID 컬럼을 참조
);


-- DEPARTMENTS01 테이블 생성
CREATE TABLE DEPARTMENTS01
(
   -- 제약조건 :: PRIMARY KEY      
   --             컬럼명 ▶『DEPARTMENT_ID』 컬럼아이디 ▶『DEPTS01_DEPT_ID_PK』
   -- 제약조건 :: CHECK(NOT NULL)  컬럼명 ▶『DEPARTMENT_NAME』 컬럼아이디 ▶『DEPTS01_DEPT_NAME』
   -- 제약조건 :: LOCATIONS01의 LOCATION_ID컬럼을  FOREIGN KEY로 사용  컬럼명 ▶『LOCATION_ID』 컬럼아이디 ▶『DEPTS01_LOC_ID_R』
    DEPARTMENT_ID      NUMBER(22)                        -- 부서 번호. 숫자입력 범위 : -10의 22승 ~ 10의 22승
,   DEPARTMENT_NAME    VARCHAR2(30)                        -- 부서 이름. 글자 입력범위 : 30바이트
,   MANAGER_ID         NUMBER(22)                        -- 담당 매니저 사번. 숫자입력 범위 : -10의 22승 ~ 10의 22승
,   LOCATION_ID        NUMBER(22)                        -- 지역별 번호. 숫자입력 범위 : -10의 22승 ~ 10의 22승
,   CONSTRAINT DEPTS01_DEPT_ID_PK PRIMARY KEY(DEPARTMENT_ID)               
,   CONSTRAINT DEPTS01_DEPT_NAME_NN  CHECK(DEPARTMENT_NAME IS NOT NULL)            
,   CONSTRAINT DEPTS01_LOC_ID_R FOREIGN KEY (LOCATION_ID) REFERENCES LOCATIONS01(LOCATION_ID)      
);

-- 제약조건 추가 -- 반드시 데이터 입력 후 실행!!
-- DEPARTMENTS01 과 EMPLOYEES01 테이블은 서로 참조하는 관계로 모두 테이블 생성시 제약조건을 설정할 수 없기 때문에
-- DEPARTMENTS01 테이블은 테이블 생성 후 제약조건을 설정하였다.

ALTER TABLE DEPARTMENTS01 
ADD CONSTRAINT DEPTS01_MANAGER_ID_R FOREIGN KEY (MANAGER_ID)
               REFERENCES EMPLOYEES01(EMPLOYEE_ID);


-- 제약조건 삭제 ( 임시 )
ALTER TABLE DEPARTMENTS01 
DROP CONSTRAINT DEPTS01_MANAGER_ID_R;


-- COUNTRIES01 테이블 생성

CREATE TABLE COUNTRIES01 --테이블 생성 
(COUNTRY_ID    CHAR(2)  /* 문자 크기2짜리 컬럼생성 */       CONSTRAINT COUNT01_COUNTRY_ID_NN NOT NULL --NOT NULL제약 조건 추가 (NOT NULL의 경우 컬럼레벨 제약조건 생성이 일반적) 
                                                                                                       -- 테이블레벨에서 NOT NULL지정시 DESCRIBE 실행시 NOT NULL표시 안됨. 
,COUNTRY_NAME  VARCHAR2(40)   /* 문자 크기40짜리 컬럼생성 */  -- 제약조건 없음                                                                         
,REGION_ID     NUMBER   --/* 크기지정을 하지 않은 『숫자』타입 컬럼 생성  */
,CONSTRAINT COUNT01_COUNTRY_ID_PK  PRIMARY KEY(COUNTRY_ID)  -- 테이블 레벨에서 『제약조건』추가 → 『CONSTRAINT NAME』 사용자가 지정한 이름 사용 가능.  
,CONSTRAINT COUNT01_REGION_ID_FK   FOREIGN KEY (REGION_ID) REFERENCES REGIONS01(REGION_ID) -- 테이블 레벨에서 『제약조건』추가 → 『CONSTRAINT NAME』 사용자가 지정한 이름 사용 가능.  
--                                  ++++++++++++            +++++++++
--                                  REGION_ID를 『참조하는 키 FK』로 제약조건 설정
--                                  ※ 테이블 레벨에서 제약조건 지정할때는 『REFERENCES』키워드만 사용 
);  



-- LOCATIONS01 테이블 생성
CREATE TABLE LOCATIONS01
( LOCATION_ID               NUMBER(4)                -- 지역별 번호, 숫자 입력 범위   : -10의 4승 ~ 10의 4승
, STREET_ADDRESS            VARCHAR2(40)             -- 거리 주소, 문자 입력 범위     : 40Byte(가변형) 
, POSTAL_CODE               VARCHAR2(12)             -- 우편 번호, 문자 입력 범위     : 12Byte(가변형)
, CITY                      VARCHAR2(30) CONSTRAINT LOC01_LOC_CITY_NN NOT NULL -- 도시명, 문자입력 범위 : 30Byte(가변형)
                                         --   CITY 컬럼에 '컬럼 레벨'의 NOT NULL 지정 후 제약조건명 생성
, STATE_PROVINCE            VARCHAR2(25)             -- 행정 주 이름 , 문자 입력 범위 : 25Byte(가변형)
, COUNTRY_ID                CHAR(2)                  -- 국가별 ID, 문자 입력 범위     : 2Byte(고정형) 
, CONSTRAINT LOC01_LOC_ID_PK PRIMARY KEY(LOCATION_ID) 
--  LOCATION_ID 컬럼에 '테이블 레벨'의 PRIMARY KEY 제약조건 생성
, CONSTRAINT LOC01_LOC_C_ID_FK FOREIGN KEY(COUNTRY_ID) REFERENCES COUNTRIES01(COUNTRY_ID)
--  COUNTRY_ID 컬럼에 '테이블 레벨'의 FOREIGN KEY 제약조건 생성 후,
--  COUNTRIES01 테이블의 COUNTRY_ID 컬럼을 참조     
);



-- JOB_HISTORY01 테이블 생성
CREATE TABLE JOB_HISTORY01
(EMPLOYEE_ID NUMBER(6)     CONSTRAINT JHIST01_EMPLOYEE_NN NOT NULL
,START_DATE  DATE          CONSTRAINT JHIST01_STRAT_DATE_NN NOT NULL
,END_DATE    DATE          CONSTRAINT JHIST01_END_DATE_NN NOT NULL
,JOB_ID      VARCHAR2(10)  CONSTRAINT JHIST01_JOB_ID_NN NOT NULL
,DEPARTMENT_ID NUMBER(4)   
,CONSTRAINT JHIST01_EMP_ID_ST_DATE01_PK PRIMARY KEY(EMPLOYEE_ID,START_DATE)    -- EMPLOYEE_ID 와 START_DATE 를 묶어서 기본키로 구성
,CONSTRAINT JHIST01_JOB_FK  FOREIGN KEY (JOB_ID)       REFERENCES JOBS01(JOB_ID)               -- JOBS01 테이블의 JOB_ID 를 참조하는 외래키
,CONSTRAINT JHIST01_EMP_FK  FOREIGN KEY (EMPLOYEE_ID)  REFERENCES EMPLOYEES01(EMPLOYEE_ID)     -- EMPLOYEES01 테이블의 EMPLOYEE_ID 를 참조하는 외래키
,CONSTRAINT JHIST01_DEPT_FK FOREIGN KEY (DEPARTMENT_ID)REFERENCES DEPARTMENTS01(DEPARTMENT_ID) -- DEPARTMENTS01 테이블의 DEPARTMENT_ID 를 참조하는 외래키
,CONSTRAINT JHIST01_DATE_INTERVAL CHECK(END_DATE > START_DATE) -- CHECK 제약조건 추가
);


-- JOBS01 테이블 생성
CREATE TABLE JOBS01
(
    JOB_ID      VARCHAR2(10)
,   JOB_TITLE   VARCHAR2(35) CONSTRAINT JOBS01_JOB_TITLE_NN NOT NULL
,   MIN_SALARY  NUMBER(22)
,   MAX_SALARY  NUMBER(22)
,   CONSTRAINT JOBS01_JOB_ID_PK  PRIMARY KEY(JOB_ID)  -- 기본키 설정
);

-- REGIONS01 테이블 생성
CREATE TABLE REGIONS01
(REGION_ID   NUMBER        CONSTRAINT REG01_REGION_ID_NN NOT NULL
,REGION_NAME VARCHAR2(25) 
,CONSTRAINT REG01_REGION_ID_PK PRIMARY KEY(REGION_ID) -- 기본키 설정
);



